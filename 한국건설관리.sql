--10.2021년도에 투입된 총 자재비용을 구하시오.

SELECT SUM(I_QTY * I_PRICE) "총 자재비용"
    FROM SITE_ITEM
    WHERE SUBSTR(I_BDATE,1,2) = '21';
    
    

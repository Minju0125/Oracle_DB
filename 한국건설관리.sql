--------------------
--1. 한국건설의 2000년대 총 공사현장은 몇개입니까?

SELECT COUNT(*) 공사현장
    FROM SITE
    WHERE S_SDATE > '2000-01-01';

-----------------------
--2. 건설현장에서 근무한 경험이 있는 사람은 몇명입니까?

SELECT COUNT (E_EMPNO) 인원수
    FROM EMPLOYEE;

-----------------------
--3. 건설 현장별로 구입자재에 대한 총 금액을 구하시오.

SELECT S_SITENO 건설현장, I_ITEMNAME 구입자재, (I_QTY * I_PRICE) "총 금액"
    FROM SITE_ITEM;

-----------------------
--4 건설현장 근무경험자 중 근무 성적이 A급 이상인 사람들을 조회하시오.

-----------------------
--5. 전체 공사 중 공사금액이 100억 이상이고, 1000억 미만인 현장을 조회하시오.

SELECT S_SITENAME 건설현장
    FROM SITE 
    WHERE S_AMOUNT >= 10000000000 AND S_AMOUNT < 100000000000;

-----------------------
--6. 2020년도 수도권(서울, 경기, 인천)에서 진행했던 공사현황을 조회하시오.

SELECT S_SITENO 사업장번호, S_SITENAME 사업장명, S_SDATE 시공일자, S_PEDATE 예상완공일, S_EDATE 완공일
    FROM SITE
    WHERE SUBSTR(S_ADDRESS,1,2) LIKE '%서울%' OR SUBSTR(S_ADDRESS,1,2) LIKE '%경기%' OR SUBSTR(S_ADDRESS,1,2) LIKE '%인천%'
    
-----------------------
--7. 2020년도 이전에 완료한 공사현장을 조회하시오.

SELECT S_SITENAME 사업장명, S_EDATE 완공일
    FROM SITE
    WHERE S_EDATE < '2020.01.01';

-----------------------
--8. 공사금액이 가장 큰 현장과 가장 작은 현장을 조회하시오.
-- 최저 최고를 한번에 나타낼 수는 없나?
    
SELECT S_SITENAME 공사현장, S_AMOUNT 공사금액
    FROM SITE
    WHERE S_AMOUNT = (SELECT MIN(S_AMOUNT) FROM SITE);
    
    
SELECT S_SITENAME 공사현장, S_AMOUNT 공사금액    
    FROM SITE
    WHERE S_AMOUNT = (SELECT MAX(S_AMOUNT) FROM SITE);
    
